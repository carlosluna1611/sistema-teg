    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{

            Connection conexion = conectar.conectarBD();
            PreparedStatement eliminar = conexion.prepareStatement("DELETE FROM registro WHERE id_registro=?");
            
            
            Statement selectStatement = conexion.createStatement();
            ResultSet resultSet = selectStatement.executeQuery("SELECT id_registro FROM registro ORDER BY id_registro ASC");
            
            int nuevoId = 1;
            while (resultSet.next())
            {
                int idActual = resultSet.getInt("id_registro");
                PreparedStatement updateStatement = conexion.prepareStatement("UPDATE registro SET id_registro = ? WHERE id_registro = ?");
                updateStatement.setInt(1, nuevoId);
                updateStatement.setInt(2, idActual);
                updateStatement.executeUpdate();
                nuevoId++;
            }
            
            resultSet.close();
            selectStatement.close();
            
            int r = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar el registro?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

            if (r == 0) {
                eliminar.setString(1, txtBusqueda.getText().trim());
                eliminar.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registro eliminado!...");

                btnRegistrar.setEnabled(true);
                btnModificar.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnConsultar.setEnabled(true);

                conectar.cerrarConexion();
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"Error: " + e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

