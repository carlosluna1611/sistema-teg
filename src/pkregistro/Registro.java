                    txtNombreAutor3.setText(consulta.getString("autorApellido_3_registro"));
                    cbLineaInvestigacion.setSelectedItem(consulta.getString("lineaIns_registro"));
                    txtAreaResumen.setText(consulta.getString("resumen_registro"));
                    txtAnoPresentacion.setText(consulta.getString("año_registro"));
                    txtNombreAsesorMetodologico.setText(consulta.getString("asesorNombreMet_registro"));
                    txtApellidoAsesorMetodologico.setText(consulta.getString("asesorApellidoMet_registro"));
